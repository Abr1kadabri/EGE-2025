# Подключение команды permutations из библиотеки itertools
from itertools import permutations

# Переставляет символы в указанном алфавите
permutations('ABC')

# Возвращает True, если все значения True
all([True, True, True])

# Возвращает позицию указанного элемента
data = [1, 2, 3]
print(data.index(2))
